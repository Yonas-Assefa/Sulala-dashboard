export const Console = {
    error: (message: any) => console.error('\x1b[41m', message),
    log: (message: any) => console.log('\x1b[42m', message),
    warn: (message: any) => console.warn('\x1b[43m', message),
    info: (message: any) => console.info('\x1b[44m', message),
    debug: (message: any) => console.debug('\x1b[45m', message),
    clear: () => console.clear(),
    reset: () => console.log('\x1b[0m'),
    time: (label: any) => console.time(label),
    timeEnd: (label: any) => console.timeEnd(label),
    trace: (label: any) => console.trace(label),
    count: (label: any) => console.count(label),
    countReset: (label: any) => console.countReset(label),
    group: (label: any) => console.group(label),
    groupCollapsed: (label: any) => console.groupCollapsed(label),
    groupEnd: () => console.groupEnd(),
    table: (data: any) => console.table(data),
    dir: (data: any) => console.dir(data),
    dirxml: (data: any) => console.dirxml(data),
    profile: (label: any) => console.profile(label),
    profileEnd: (label: any) => console.profileEnd(label),
    timeStamp: (label: any) => console.timeStamp(label),
    assert: (condition: boolean, message: any) => console.assert(condition, message),
    clearGroup: () => console.clear(),
    clearConsole: () => console.clear(),
    clearProfile: () => console.profileEnd(),
    clearTimeStamp: () => console.clear(),
    clearContext: () => console.clear(),
    clearAssert: () => console.clear(),
    clearAll: () => console.clear(),
}